/**
 * Build DIY image manifest from diyActivities.js.
 * Run: node scripts/build-diy-image-manifest.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import diyActivities from '../src/data/diyActivities.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outPath = path.join(__dirname, '../src/data/diyImageManifest.js');

const CATEGORY_FALLBACK = {
  sensory: { accent: '#C2603E', from: '#EFD2C4', to: '#F5ECE0' },
  motor: { accent: '#5A9FD8', from: '#D6E9F8', to: '#EEF5FF' },
  cognitive: { accent: '#9B88C0', from: '#E8E0F0', to: '#F5F0FF' },
  emotional: { accent: '#C77A5C', from: '#F3D9CC', to: '#FFF0F3' },
  bonding: { accent: '#5FB878', from: '#D4ECD9', to: '#F0FFF4' },
};

function buildPrompt(name, materials) {
  const props = materials.slice(0, 3).join(', ');
  return [
    `Editorial baby activity: ${name}.`,
    `Scene shows ${props}.`,
    'Hands-only or minimal luxury home setting, warm natural light, desaturated warm tones,',
    'magazine editorial, candid not staged. Horizontal 4:3, no text, no logos, no stock baby clichés.',
  ].join(' ');
}

function buildAlt(name) {
  return `Editorial photo for ${name} baby activity`;
}

const manifest = {};
const activityManifest = {};

diyActivities.forEach((monthBlock) => {
  monthBlock.activities.forEach((activity) => {
    const category = activity.category;
    const palette = CATEGORY_FALLBACK[category] || CATEGORY_FALLBACK.sensory;
    const prompt = buildPrompt(activity.name, activity.materials);
    const alt = buildAlt(activity.name);

    activityManifest[activity.id] = {
      activityId: activity.id,
      name: activity.name,
      month: monthBlock.month,
      illustration: activity.illustration,
      category,
      materials: activity.materials.slice(0, 3),
      prompt,
      alt,
    };

    if (manifest[activity.illustration]) return;
    manifest[activity.illustration] = {
      key: activity.illustration,
      activityName: activity.name,
      category,
      materials: activity.materials.slice(0, 3),
      prompt,
      alt,
      accent: palette.accent,
      gradientFrom: palette.from,
      gradientTo: palette.to,
    };
  });
});

const sortedKeys = Object.keys(manifest).sort();
const illustrationEntries = sortedKeys.map((key) => [key, manifest[key]]);

const activityIds = Object.keys(activityManifest).sort((a, b) => {
  const monthA = activityManifest[a].month;
  const monthB = activityManifest[b].month;
  if (monthA !== monthB) return monthA - monthB;
  return a.localeCompare(b);
});
const activityEntries = activityIds.map((id) => [id, activityManifest[id]]);

const fileContents = `/**
 * DIY image manifest — auto-generated by scripts/build-diy-image-manifest.mjs
 * Do not edit by hand; re-run the script after diyActivities.js changes.
 */
export const diyImageManifest = Object.fromEntries([
${illustrationEntries.map(([key, entry]) => `  ['${key}', ${JSON.stringify(entry, null, 2).replace(/\n/g, '\n  ')}],`).join('\n')}
]);

export const diyImageKeys = ${JSON.stringify(sortedKeys)};

/** Per-activity metadata (180 rows) — keyed by activity.id */
export const diyActivityImages = Object.fromEntries([
${activityEntries.map(([id, entry]) => `  ['${id}', ${JSON.stringify(entry, null, 2).replace(/\n/g, '\n  ')}],`).join('\n')}
]);

export const diyActivityIds = ${JSON.stringify(activityIds)};
`;

fs.writeFileSync(outPath, fileContents);
console.log(`Wrote ${sortedKeys.length} illustration keys, ${activityIds.length} activity entries → src/data/diyImageManifest.js`);
